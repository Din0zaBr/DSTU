package stepick.Java.Java_from_Zero_to_Junior_plus_Interview_preparation.Introduction;

public class HelloWorld {
    public static void main(String[] args) { // Это точка входа в прогу, а также начало первого метода

        System.out.println("Hello World!"); // System.out.println("...") - вывод чего-то, что в кавычках
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!"); // конец первого метода
    }
// коротко написать public static void main - psvm
// public - модификатор (ключевое слово), который запускает любой пользователь (потому что публичный, логично лол, да?)
// static - модификатор, который допускает вызов метода (main) до создания объектов класса. Без него никак, так как это база
// void - модификатор, который сообщает, что метод main ничего не возвращает
}

