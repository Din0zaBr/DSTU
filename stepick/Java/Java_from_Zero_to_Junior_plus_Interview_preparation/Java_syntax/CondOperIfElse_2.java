public class CondOperIfElse_2 {
    public static void main(String[] args) {
        int temp = 24;
        if (temp > 25) {
            System.out.println("Кондиционер включён");
        } else if (temp < 22) {
            System.out.println("Кондиционер выключен");
        } else {
            System.out.println("Кондиционер ничего не делает");
        }

    }
}
