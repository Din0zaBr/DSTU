package stepick.Java.Java_from_Zero_to_Junior_plus_Interview_preparation.Java_syntax;

public class FloatDouble_4 {
    public static void main(String[] args) {
        float radius = 10.8f; // по умолч. 10.8 - double, чтобы преобразовать нужно поставить f в конец дроб. числа
        float pi = 3.14f;
        float area = pi * radius * radius;
        System.out.println(area);
    }
}
