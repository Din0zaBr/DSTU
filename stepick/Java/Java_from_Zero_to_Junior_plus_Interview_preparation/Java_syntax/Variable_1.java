package stepick.Java.Java_from_Zero_to_Junior_plus_Interview_preparation.Java_syntax;

public class Variable_1 {
    public static void main(String[] args) {
        int x = 5;
        int y = x + 5;
        System.out.println(y);

        int john = 100;
        int nick = john * 5;
        int sum_it_up = john + nick;
        System.out.println(sum_it_up);

        int johna = 100;
        int nickola = john * 5;
        int minus_it_up = johna - nickola;
        System.out.println(minus_it_up);

        int a = 10;
        int b = 3;
        int c = a / b; // 3, так как c - целое число
        System.out.println(c);

        int d = 10;
        int f = 3;
        int e = d % f; // 1, так как c - целое число
        System.out.println(e);
    }
}
